package shapes;

public class Shapes {

	private int length;
	private int width;
	private int height;
	private String name;
	
	public Shapes(int l, int w, int h, String n)
	{
		length = l;
		width = w;
		height = h;
		name = n;
	}
	
	public int getLength()
	{
		return length;
	}
	
	public int getWidth()
	{
		return width;
	}
	
	public int getHeight()
	{
		return height;
	}
	
	public String getName()
	{
		return name;
	}
	
	
	
}
